void OLED_Set_Pos(unsigned char x, unsigned char y)
{
	uint8_t Set_Pos[6]={0x80,0x00,0x80,0x00,0x00,0x00};

	Set_Pos[1]=0xB0+y;
	Set_Pos[3]=((x&0xf0)>>4)|0x10;
	Set_Pos[5]=(x&0x0f);
	i2c.write(OLED_I2C_WRITE,(char*)Set_Pos,6);
}

void OLED_Clear(void)
{
	unsigned char i;
	uint8_t zero[256] = {0};

	for(i=0;i<253;i=i+2){
    zero[i] = 0xC0;        /* command      */
		zero[i+1] = 0x00;      /* value = ' '  */
	}
	zero[254]=0x40;          /* End command  */

	for(i=0;i<8;i++){
    OLED_Set_Pos(0,i);
		i2c.write(OLED_I2C_WRITE,(char*)zero,256);
	}
}


